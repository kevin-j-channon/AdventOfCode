class Header
{
public:

	Header(std::istream& is)
		: _version{ read_bit_value<3>(is) }
		, _type{ static_cast<PacketType>(read_bit_value<3>(is))}
	{
	}

	uint8_t version() const { return _version; }
	PacketType type() const { return _type; }

private:

	uint8_t _version;
	PacketType _type;
};

